      <p>This policy explains how you can request deletion of your Schoozie account and associated personal data, what is deleted, and what we may retain. It supports the account-deletion requirements of the Apple App Store and Google Play.</p>

      <h2>1. How to request deletion</h2>
      <ul>
        <li><strong>In the app:</strong> open Profile / Settings and use the account-deletion option where available; or</li>
        <li><strong>By email:</strong> write to <a href="mailto:hello@schoozie.com?subject=Data%20Deletion%20Request">hello@schoozie.com</a> from your registered email or with your registered phone/login ID, with the subject &ldquo;Data Deletion Request&rdquo;.</li>
        <li>Parents and staff whose data is managed by a school may also ask their school to remove their records; the school (as data controller) can action this in the platform.</li>
      </ul>

      <h2>2. What we will delete</h2>
      <p>On a verified request we will delete or irreversibly anonymise the personal information tied to your account, including profile details, contact information and login credentials, and remove your access to the Services.</p>

      <h2>3. What we may retain</h2>
      <p>We may retain limited information where the law requires it or where we have a legitimate need, for example:</p>
      <ul>
        <li>Financial and transaction records needed for accounting, tax and audit obligations;</li>
        <li>Records needed to resolve disputes, prevent fraud or enforce our Terms;</li>
        <li>Backup copies for a limited period until they are cycled out;</li>
        <li>Data that belongs to a school as controller (e.g. a student&rsquo;s academic record) may be retained by the school even after an individual user&rsquo;s login is removed.</li>
      </ul>

      <h2>4. Timelines</h2>
      <p>We aim to acknowledge deletion requests within a few working days and to complete verified deletions within thirty (30) days, except for data retained under Section 3. We may ask you to verify your identity before acting on a request.</p>

      <h2>5. Effect of deletion</h2>
      <p>Deleting your account is permanent. You will lose access to the Services and to information associated with your account. Where you have multiple roles or children linked to one account, please tell us the scope of your request.</p>

